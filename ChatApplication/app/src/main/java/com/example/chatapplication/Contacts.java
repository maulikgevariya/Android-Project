package com.example.chatapplication;

public class Contacts {

    public String name;
    public String status;
    public String image;

    public Contacts(){

    }

    public Contacts(String name, String status, String image) {
        this.name = name;
        this.status = status;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public String getStatus() {
        return status;
    }

    public String getImage() {
        return image;
    }
}
